package com.example.pos_report.database.model;

public class Staffs {

	int StaffID;
	String StaffCode;
	String StaffName;
	String StaffPassword;
	int StaffRoleID;
	String StaffRoleName;
	public int getStaffID() {
		return StaffID;
	}
	public String getStaffCode() {
		return StaffCode;
	}
	public String getStaffName() {
		return StaffName;
	}
	public String getStaffPassword() {
		return StaffPassword;
	}
	public int getStaffRoleID() {
		return StaffRoleID;
	}
	public String getStaffRoleName() {
		return StaffRoleName;
	}
	public void setStaffID(int staffID) {
		StaffID = staffID;
	}
	public void setStaffCode(String staffCode) {
		StaffCode = staffCode;
	}
	public void setStaffName(String staffName) {
		StaffName = staffName;
	}
	public void setStaffPassword(String staffPassword) {
		StaffPassword = staffPassword;
	}
	public void setStaffRoleID(int staffRoleID) {
		StaffRoleID = staffRoleID;
	}
	public void setStaffRoleName(String staffRoleName) {
		StaffRoleName = staffRoleName;
	}
	
	
	
}
